	ORG	00	; DEFINE A PRIMEIRA INSTRUÇÃO DO PROGRAMA
	JMP	inicio	; DESVIA A EXECUÇÃO DO PROGRAMA PARA A LINHA
			; CHAMADA "inicio"
	DB	20	; ENDEREÇO DE INÍCIO DA ROTINA DE SERVIÇO
			; DA INTERRUPÇÃO DO TIMER
	DB	40	; ENDEREÇO DE INÍCIO DA ROTINA DE SERVIÇO
			; DA INTERRUPÇÃO DO TECLADO
	DB	60	; ENDEREÇO DE INÍCIO DA ROTINA DE SERVIÇO
			; DA INTERRUPÇÃO DO PAD
inicio:	OUT	07	; MOSTRA O TECLADO ALFANUMÉRICO
	OUT	08	; MOSTRA O PAD NUMÉRICO
	MOV	BL,C0	; GUARDA O END. DA 1a. POS. DA MEM.
			; DE VÍDEO NO REGISTRADOR BL
	MOV	AL,20	; GUARDA O EQUIVALENTE ASCII DO ESPAÇO EM BRANCO
	STI		; HABILITA O USO DE INTERRUPÇÕES
loop:	NOP		; INSTRUÇÃO NULA (NÃO FAZ NADA...)
	JMP	loop	; DESVIA A EXECUÇÃO DO PROGRAMA PARA A 
			; LINHA CHAMADA LOOP

	ORG	20	; INÍCIO DA ROTINA DE SERVIÇO DO TIMER
	CLI		; DESABILITA AS INTERRUPÇÕES
	MOV	[BL],AL	; MOSTRA O VALOR ASCII DO REG. AL NA MEMÓRIA DE VÍDEO
	STI		; HABILITA O USO DE INTERRUPÇÕES
	IRET		; RETORNA PARA O PROGRAMA PRINCIPAL

	ORG	40	; INÍCIO DA ROTINA DE SERVIÇO DO TECLADO
	CLI		; DESABILITA AS INTERRUPÇÕES
	IN	07	; CAPTURA O DADO DO TECLADO ALFANUMÉRICO
			; E ARMAZENA ESSE DADO NO REGISTRADOR AL
	STI		; HABILITA O USO DE INTERRUPÇÕES
	IRET		; RETORNA PARA O PROGRAMA PRINCIPAL

	ORG	60	; INÍCIO DA ROTIMA DE SERVIÇO DO PAD
	CLI		; DESABILITA AS INTERRUPÇÕES
	IN	08	; CAPTURA O DADO DO PAD NUMÉRICO
			; E ARMAZENA ESSE DADO NO REGISTRADOR AL
	STI		; HABILITA O USO DE INTERRUPÇÕES
	IRET

	END




